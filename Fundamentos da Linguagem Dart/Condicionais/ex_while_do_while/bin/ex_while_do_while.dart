import 'package:ex_while_do_while/ex_while_do_while.dart' as ex_while_do_while;

void main(List<String> arguments) {
  // Exemplo de uso do while
  var i = 0;
  while (i < 5) { // enquanto i < 5 faça
    print(i);
    i++;
  }

  // Exemplo de uso do do while
  i = 0;
  do {
    print(i);
    i++;
  } while (i < 5); // faça enquanto i < 5
}
